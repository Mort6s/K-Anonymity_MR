package Incognito_SQLExpress_Census;

public class EiColumn {

	int EiStart;
	int EiEnd;
	
	public EiColumn(){};
	
	public void setEiStart(int _EiStart){
		EiStart = _EiStart;
	}
	
	public void setEiEnd(int _EiEnd){
		EiEnd = _EiEnd;
	}
	
	public int getEiStart(){
		return EiStart;
	}
	
	public int getEiEnd(){
		return EiEnd;
	}
	
}
